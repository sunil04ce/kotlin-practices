
fun main(args: Array<String>){

    /*
    var : can reassign values
    val: can not reassign values it is same as final in java
    */

    var myName = "Sunil"
    myName = "James"

    // Final variables
    val pi = 3.14
    val url = "www.google.com"

    println("myName is $myName")
    println("Variables are final :  pi = $pi and url = $url ")

}